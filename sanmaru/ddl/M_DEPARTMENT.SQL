-- CREATE  TABLE `部署管理マスタ`
DROP
TABLE IF EXISTS `M_DEPARTMENT`;
CREATE
TABLE `M_DEPARTMENT` (
     `DEPARTMENT_ID` varchar(5) NOT NULL COMMENT '部署管理ID'
,     `DEPARTMENT_NAME` varchar(20) NOT NULL COMMENT '部署管理名'
,     `RESPONSIBLE_PERSON` varchar(15) NOT NULL COMMENT '責任者'
,     `ESTABLISHMENT_DATE` date NOT NULL COMMENT '設立日'
,     `REMARKS` varchar(30) COMMENT '備考'
,     `CREATEUSERID` varchar(10) COMMENT '登録者'
,     `CREATETIME` timestamp COMMENT '登録日時'
,     `UPDATEUSERID` varchar(10) COMMENT '更新者'
,     `UPDATETIME` timestamp COMMENT '更新日時'
,CONSTRAINT PRIMARY KEY (`DEPARTMENT_ID`)

) ENGINE = InnoDB DEFAULT CHARSET=utf8;
